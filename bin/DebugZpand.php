  <h1>Welcome to Touch Typing Study!</h1>

<div id="first">

<div id="intro">
<p><p>Are you still typing with two fingers? Do you still need to look at your keyboard before every keystroke?</p><p> </p><p>Well, Touch Typing Study is a free, user-friendly learning website that is designed to help you learn, practice and improve your typing speed and accuracy. Once you can touch type you will not need to look at the keyboard to find the letters you want to type and you will also be able to type at a much faster speed!</p><p>Touch typing is a method based on muscle memory instead of sight. This method allows you to achieve much higher speed of data entry, especially if you need to transcribe text from other visual material.</p><p>Typing with the touch typing method significantly improves your computer productivity; it increases data entry speed and, where possible, reduces fatigue and injury to the eyes.</p><p>Touch Typing Study contains 15 lessons, a speed test and games from which you can learn to type step-by-step, monitor your own progress and have fun!</p><p></div>
